import Navbar from "../components/Navbar";
import { useNavigate } from "react-router-dom";
import { jsPDF } from "jspdf";

function Result({ score, totalQuestions }) {
  const navigate = useNavigate();

  let message = "";

  if (score === totalQuestions) {
    message = "Excellent!";
  } else if (score >= totalQuestions / 2) {
    message = "Good Job!";
  } else {
    message = "Keep Practicing!";
  }

  const percentage = ((score / totalQuestions) * 100).toFixed(0);

  // Save Quiz History
  const saveHistory = () => {
    const interest = localStorage.getItem("interest");

    const history = JSON.parse(localStorage.getItem("history")) || [];

    history.push({
      interest: interest || "Unknown",
      score,
      totalQuestions,
      date: new Date().toLocaleString(),
    });

    localStorage.setItem("history", JSON.stringify(history));

    navigate("/dashboard");
  };

  // Download PDF
  const downloadPDF = () => {
    const doc = new jsPDF();

    const interest = localStorage.getItem("interest") || "Unknown";

    doc.setFontSize(20);
    doc.text("AI Integrated Quiz Platform", 20, 20);

    doc.setFontSize(16);
    doc.text("Quiz Report", 20, 35);

    doc.setFontSize(12);

    doc.text(`Interest: ${interest}`, 20, 50);
    doc.text(`Score: ${score} / ${totalQuestions}`, 20, 60);
    doc.text(`Percentage: ${percentage}%`, 20, 70);
    doc.text(`Performance: ${message}`, 20, 80);
    doc.text(`Date: ${new Date().toLocaleString()}`, 20, 90);

    doc.line(20, 100, 190, 100);

    doc.setFontSize(14);
    doc.text("AI Recommendation", 20, 115);

    doc.setFontSize(12);

    if (percentage >= 80) {
      doc.text(
        "Excellent performance! Keep solving advanced quizzes.",
        20,
        130
      );
    } else if (percentage >= 50) {
      doc.text(
        "Good work! Practice regularly to improve your score.",
        20,
        130
      );
    } else {
      doc.text(
        "Revise the basics and attempt more quizzes.",
        20,
        130
      );
    }

    doc.line(20, 150, 190, 150);

    doc.text("Generated by AI Integrated Quiz Platform", 20, 165);

    doc.save("Quiz_Report.pdf");
  };

  return (
    <>
      <Navbar />

      <div className="container">
        <div className="card">

          <h1>Quiz Completed</h1>

          <h2>Your Score</h2>

          <div
            style={{
              width: "140px",
              height: "140px",
              borderRadius: "50%",
              background: "linear-gradient(135deg,#60a5fa,#2563eb)",
              margin: "20px auto",
              display: "flex",
              flexDirection: "column",
              justifyContent: "center",
              alignItems: "center",
              color: "white",
              boxShadow: "0 10px 25px rgba(37,99,235,.3)",
            }}
          >
            <h1 style={{ margin: 0 }}>{score}</h1>

            <p style={{ margin: 0 }}>
              / {totalQuestions}
            </p>
          </div>

          <h3>{message}</h3>

          <div
            style={{
              display: "inline-block",
              background: "#dbeafe",
              color: "#1d4ed8",
              padding: "8px 20px",
              borderRadius: "30px",
              fontWeight: "bold",
              marginTop: "10px",
            }}
          >
            {percentage}% Score
          </div>

          <div
            style={{
              marginTop: "25px",
              padding: "20px",
              borderRadius: "15px",
              background: "#f8fafc",
              textAlign: "left",
              border: "1px solid #dbeafe",
            }}
          >
            <h3>🤖 AI Recommendation</h3>

            <p>
              {percentage >= 80
                ? "Excellent performance! Keep challenging yourself with advanced quizzes."
                : percentage >= 50
                ? "Good work! Practice consistently to improve your score."
                : "Revise the basics and solve more quizzes to strengthen your understanding."}
            </p>
          </div>

          <h3>Percentage : {percentage}%</h3>

          <br />

          <button onClick={downloadPDF}>
            📄 Download PDF
          </button>

          <br />
          <br />

          <button onClick={() => navigate("/quiz")}>
            🔄 Play Again
          </button>

          <br />
          <br />

          <button onClick={saveHistory}>
            🏠 Dashboard
          </button>

        </div>
      </div>
    </>
  );
}

export default Result;